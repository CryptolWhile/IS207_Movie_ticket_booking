export function getShowtimesByMovieId(movieId: number): Showtime[] {
  return showtimes.filter((showtime) => showtime.movieId === movieId)
}

export function getShowtimesByDate(date: string): Showtime[] {
  return showtimes.filter((showtime) => showtime.date === date)
}

export function getShowtimesByBranchAndDate(branch: "HCM" | "HN", date: string): Showtime[] {
  return showtimes.filter((showtime) => showtime.branch === branch && showtime.date === date)
}

export function getShowtimesByBranch(branch: "HCM" | "HN"): Showtime[] {
  return showtimes.filter((showtime) => showtime.branch === branch)
}
